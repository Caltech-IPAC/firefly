public class InputTag {
    String locstr;
    String surveys;
    String subsetsize;
    String orientation;
    String reproject;
    String grid;
    String marker;
    String shrunkgrid;
    String shrunkmarker;

    public String getLocstr() {
        return locstr;
    }

    public void setLocstr(String locstr) {
        this.locstr = locstr;
    }

    public String getSurveys() {
        return surveys;
    }

    public void setSurveys(String surveys) {
        this.surveys = surveys;
    }

    public String getSubsetsize() {
        return subsetsize;
    }

    public void setSubsetsize(String subsetsize) {
        this.subsetsize = subsetsize;
    }

    public String getOrientation() {
        return orientation;
    }

    public void setOrientation(String orientation) {
        this.orientation = orientation;
    }

    public String getReproject() {
        return reproject;
    }

    public void setReproject(String reproject) {
        this.reproject = reproject;
    }

    public String getGrid() {
        return grid;
    }

    public void setGrid(String grid) {
        this.grid = grid;
    }

    public String getMarker() {
        return marker;
    }

    public void setMarker(String marker) {
        this.marker = marker;
    }

    public String getShrunkgrid() {
        return shrunkgrid;
    }

    public void setShrunkgrid(String shrunkgrid) {
        this.shrunkgrid = shrunkgrid;
    }

    public String getShrunkmarker() {
        return shrunkmarker;
    }

    public void setShrunkmarker(String shrunkmarker) {
        this.shrunkmarker = shrunkmarker;
    }
}
